<p>酒类展区<span>: 白酒、葡萄酒、啤酒、黄酒、果酒、保健酒/药酒、米酒、预调酒等酒类产品；</span></p>
<p>食品展区<span>:</span><span> </span>休闲食品、烘焙食品、奶蛋制品、肉类制品、水产品、农副产品、粮油制品；</p>
<p><b>&nbsp;&nbsp;&nbsp; </b>调味品展区：酱油、食醋、腐乳鸡精、味精、料酒、香辛料、酱腌菜、酱、复合调味料、火锅调料、食糖、食盐、各类食用油、各类罐头、香精香料等；</p>
<p>饮料展区<span>:</span><span> </span>碳酸类饮料、果蔬汁饮料、功能饮料、茶类饮料、咖啡、矿泉水、非酒精饮料等饮料产品；</p>
<p>茶叶展区<span>:</span><span> </span>乌龙茶、红茶、绿茶、白茶、黑茶等各类茶叶、茶制品及各茶具工艺制品。</p>
